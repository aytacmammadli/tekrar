public class Human {
  private String sacRengi;
  private   int yas;
 private    int ceki;


  public Human(String sacRengii, int yas, int ceki) {
    this.sacRengi = sacRengii;
    this.yas = yas;
    this.ceki = ceki;
  }

  public String getSacRengi() {
    return sacRengi;
  }

  public int getYas() {
    return yas;
  }

  public int getCeki() {
    return ceki;
  }

  public void setSacRengi(String sacRengi) {
    this.sacRengi = sacRengi;
  }

  public void setYas(int yas) {
    this.yas = yas;
  }

  public void setCeki(int ceki) {
    this.ceki = ceki;
  }
}
